import React, { useState } from 'react';
import styled from 'styled-components';
import { BsFacebook } from 'react-icons/bs';

function Facebook({ selectedStyle, productName, colorScheme }) {
  const styleName = selectedStyle.name.replace('&', 'and');

  const [color, setColor] = useState('#4267B2');

  const facebookPost = `Look what I just found! It's
  ${styleName} ${productName} you can find here on Nile.
  This message was automatically generated by a share button on a website I am working on
  with a team of awesome and talented fellow student developers, so you can safely ignore this post
  and any others like it if/when I run more tests. Have a great day!`;

  const baseUrl = 'http://www.facebook.com/sharer/sharer.php';
  const linkToShare = '?u=localhost:3000';

  const fullUrl = baseUrl + linkToShare;

  return (
    <FacebookIcon onMouseEnter={() => setColor(colorScheme.textColorBackground)} onMouseLeave={() => setColor('#4267B2')}>
      <a href={fullUrl} target="_blank" rel="noreferrer">
        <BsFacebook size={25} color={color} />
      </a>
    </FacebookIcon>
  );
}

const FacebookIcon = styled.div`
  display: flex;
  justify-content: center;
  align-items: center;
  margin-bottom: 5px;
`;

export default Facebook;

